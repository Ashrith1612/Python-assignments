# Use the std dev function of the statistics module to compute the mean and std dev of
# a list of numbers [10,20,30,40,50,60]

#import statistics library
import statistics

# Calculate the standard deviation from a sample of data
print(statistics.stdev([10,20,30,40,50,60]))

# Calculate mean

print (statistics.mean([10,20,30,40,50,60]))